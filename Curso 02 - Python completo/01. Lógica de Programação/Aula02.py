#Conteudo da aula: Tipo String, int, Float e boolean 

# Aspas simples
print('Arisio Andrade')
# Aspas duplas
print("Arisio Andrade")
# Escape
print("Arisio \"Andrade\"")
# Aspas no texto
print('Arisio "Andrade"')


# int -> Número inteiro
# Representa um número qualquer(positivo ou negativo)
print(7)
print(-7)

# float -> Número com ponto flutuante
# Representa um número qualquer(positivo ou negativo com ponto flutuante)
print(7.7)
print(-7.7 )

# A função type mostra o tipo de valor
print(type("Arisio"))
print(type(7))

# Boolean
# Usado para questionar algo em um programa(se é true ou false)
# Existem operadores para "questionar", como por exemplo: ==
print(10 == 10)
print(10 != 10)