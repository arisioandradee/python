#Conteudo da aula: padding/espaçamento em Strings

variavel = 'ABC'

print(f'{variavel}')
print(f'{variavel: >10}') #Texto a esquerda
print(f'{variavel: <10}') #Texto a direita
print(f'{variavel: ^10}') #Texto no centro
print(f'{variavel:0>10}') #Usando 0 em vez do espaço 
print(f'O hexadecimal de 1500 é: {1500:08X}')



