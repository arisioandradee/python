'''
Introdução ás funções (def) em Python
Funções são trechos de código usados para replicar determinada ação ao longo do cod
Elas podem receber valores para parâmetros(args) e retornar um valor especifico
Por padrão, retornam none (nada)
'''

def soma (a,b):
    print(f'{a+b}')

soma(5,10)
soma(20,30)
soma(10, 10)
